const enKeys = Object.keys(TRANSLATIONS.en);
for (const locale of ["no", "da", "de", "fr", "es"]) {
  for (const key of enKeys) {
    if (!TRANSLATIONS[locale][key]) {
      TRANSLATIONS[locale][key] = TRANSLATIONS.en[key];
    }
  }
}

export function getTranslations(locale: string): Record<string, string> {
  return {
    ...TRANSLATIONS.en,
    ...(TRANSLATIONS[locale] || {}),
  };
}

/**
 * Standalone translate function when locale is passed.
 * Use useLanguage().t(key) from LanguageContext when inside React components.
 */
export function t(locale: string, key: string): string {
  const trans = getTranslations(locale);
  return trans[key] ?? TRANSLATIONS.en[key] ?? key;
}

export function localeFromNavigator(): LocaleCode {
  if (typeof navigator === "undefined") return "en";
  const lang = navigator.language || (navigator as { userLanguage?: string }).userLanguage || "en";
  const code = lang.split("-")[0].toLowerCase();
  const found = ALL_LOCALES.find((l) => l.code === code);
  return (found ? found.code : "en") as LocaleCode;
}
